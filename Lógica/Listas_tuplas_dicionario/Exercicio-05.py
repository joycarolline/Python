'''5. Crie duas tuplas. Concatene-as para formar uma nova tupla.'''

primeira_tupla = ('maça', 'morango', 'abacaxi')
segunda_tupla = ('banana', 'limão', 'pera')

print(f"A primeira tupla é {primeira_tupla}")
