'''Faça um Programa que peça dois números, realize as principais operações soma, 
subtração, multiplicação, divisão.'''


print("Descubra o resultado das principais operações matemáticas entre dois numeros:  \n")

num1 = int(input("Digite o primeiro número: " ));
num2 = int(input("Digite o primeiro número: " ));

soma = num1+num2;
sub = num1-num2;
mult = num1*num2;
div = num1/num2;

print(f"O resultado é: \n soma = {soma}; \n subtração = {sub}; \n multiplicação = {mult}; \n divisão = {div}")

